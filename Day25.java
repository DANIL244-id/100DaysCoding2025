public class day25 {
    public static void main(String[] args) {
       //Increment (++)
        int a = 9;
        System.out.println("Nilai awal a = "+a);
        a++;
        System.out.println("Setelah Increment nilai a menjadi = "+a);
        
        //Decrement (--)
        int b = 5;
        System.out.println("Nilai awal b = "+b);
        b--;
        System.out.println("Setelah Decrement nilai b menjadi = "+b);

    }

}
