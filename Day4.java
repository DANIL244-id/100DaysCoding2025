public class day4 {

  public static void main(String[] args) {
     // /n dipakai untuk membuat baris baru
    System.out.println("baris pertama\nbaris kedua\nbaris ketiga");

     // \t dipakai untuk menambahkan spasi tab (sama seperti menekan tombol tab di keyboard)
    System.out.println("nama  \t  : DANIL");
    System.out.println("alamat \t : desa baru");

     // \" dipakai untuk menuliskan tanda kutip ganda (")
    System.out.println("nama saya, \"DANIL\"");
    System.out.println("alamat saya, \"desa baru\"");

     /* \\ (blacklash)
          fungsinya untuk menuliskan tanda blacklash(\)itu sendiri
       */
    System.out.println("Folder: C:\\Users\\day4");


  }
  
}
